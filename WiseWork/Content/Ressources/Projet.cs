using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TestWebApi.Controllers.Ressources
{
    public class Projet
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Variables
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private string nom;
        private List<Utilisateur> utilisateur;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Constructeurs
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Projet()
        {
            initialise();
        }

        public Projet(string nom)
        {
            initialise();

            this.nom = nom;
        }

        public Projet(string nom, List<Utilisateur> listUtilisateur)
        {
            initialise();

            this.nom = nom;
            this.utilisateur = listUtilisateur;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Initialise
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private void initialise()
        {
            this.nom = null;
            this.utilisateur = new List<Utilisateur>();
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Methodes
        ////////////////////////////////////////////////////////////////////////////////////////////////////
    }
}
