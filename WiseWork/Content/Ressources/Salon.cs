using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace WiseWork.Content.Ressources
{
    public class Salon
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Variables
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private int id;
        private string nom;
        private List<Utilisateur> utilisateur;
        private List<string> listMessage;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Constructeurs
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Salon()
        {
            initialise();
        }

        public Salon(int id, string nom)
        {
            initialise();

            this.id = id;
            this.nom = nom;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Initialise
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private void initialise()
        {
            this.nom = null;
            this.utilisateur = new List<Utilisateur>();
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Methodes
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Nom
        {
            get { return nom; }
            set { nom = value; }
        }

        public List<string> ListMessage
        {
            get { return listMessage; }
            set { listMessage = value; }
        }

        public int Id
        {
            get { return id; }
            set { id = value; }
        }
    }
}
