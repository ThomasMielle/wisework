using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TestWebApi.Controllers.Ressources
{
    public class Commentaire
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Variables
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private Utilisateur auteur;
        private DateTime dateHeurePublication;

        private string message;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Constructeurs
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public Commentaire()
        {
            initialise();
        }

        public Commentaire(Utilisateur auteur, DateTime dateHeurePublication, string message)
        {
            initialise();

            this.auteur = auteur;
            this.dateHeurePublication = dateHeurePublication;
            this.message = message;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Initialise
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        private void initialise()
        {
            this.auteur = new Utilisateur();
            this.dateHeurePublication = new DateTime();
            this.message = null;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        //  Methodes
        ////////////////////////////////////////////////////////////////////////////////////////////////////
    }
}
